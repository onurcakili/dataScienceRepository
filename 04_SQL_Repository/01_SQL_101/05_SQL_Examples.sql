-- 1) EMPLOYEES TABLOSUNDAKİ KAYITLARI ID ALANINA GÖRE KÜCÜKTEN BÜYÜĞE SIRALAYAN SQL SORGUSU
SELECT * FROM EMPLOYEES
ORDER BY ID

-- 2) EMPLOYEES TABLOSUNDAKİ KAYITLARI ID ALANINA GÖRE BÜYÜKTEN KÜÇÜĞE SIRALAYAN SQL SORGUSU
SELECT * FROM EMPLOYEES
ORDER BY ID DESC

-- 3) EMPLOYEES TABLOSUNDAKİ KAYITLARI AD VE SOYAD ALANINA GÖRE A'DAN Z'YE SIRALAYAN SQL SORGUSU
SELECT * FROM EMPLOYEES
ORDER BY FULLNAME 

-- 4) EMPLOYEES TABLOSUNDAKİ KAYITLARI İL VE İLÇEYE GÖRE SIRALAYAN SQL SORGUSU
SELECT * FROM EMPLOYEES
ORDER BY CITY, TOWN

-- 5) EMPLOYEES TABLOSUNDA ANKARA'DA YAŞAYANLARI YAŞINA GÖRE SIRALAYAN SQL SORGUSU
SELECT * FROM EMPLOYEES
WHERE CITY = 'ANKARA'
ORDER BY AGE

-- 6) EMPLOYEES TABLOSUNDA ANKARA'DA YAŞAYANLARI DOĞUM TARİHLERİNE GÖRE SIRALAYAN SQL SORGUSU 
SELECT * FROM EMPLOYEES
WHERE CITY = 'ANKARA'
ORDER BY BIRTHDATE

-- 7) EMPLOYEES TABLOSUNDA İSTANBUL'DA YAŞAYANLARI ALIAS KULLANARAK 
	 --TÜRKÇE KOLON İSİMLERİ YAZDIRAN, İLÇE VE YAŞA GÖRE SIRALAYAN SQL SORGUSU
   
SELECT 
CITY AS İL,
TOWN AS İLÇE,
AGE AS YAŞ FROM EMPLOYEES
WHERE CITY = 'ISTANBUL' 
ORDER BY TOWN, AGE

