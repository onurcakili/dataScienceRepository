-- 1) EMPLOYEES TABLOSUNDAN KAÇ TANE TEKİL ŞEHİR OLDUĞUNU GETİREN SQL SORGUSU
SELECT DISTINCT CITY FROM EMPLOYEES

-- 2) EMPLOYEES TABLOSUNDAN İZMİR'İN KAÇ TANE İLÇESİNİ OLDUĞUNU GETİREN SQL SORGUSU
SELECT DISTINCT TOWN FROM EMPLOYEES
WHERE CITY = 'İZMİR'

-- 3) EMPLOYEES TABLS. AGE ALANI EKLEYIN VE BUNU BIRTHDAY'E GÖRE HESAPLAYAN SQL SORGUSU
UPDATE EMPLOYEES
SET AGE = DATEDIFF(year,BIRTHDATE, GETDATE())

-- 4) ANKARADA YAŞAYANLARIN YAŞ ÇEŞİTLİLİĞİNİ GETİREN SQL SORGUSU
SELECT DISTINCT AGE FROM EMPLOYEES
WHERE CITY = 'ANKARA'

