-- 1) EMPLOYEES TABLOSUNDA EN YAŞLI 10 KİŞİYİ GETİREN SQL SORGUSU
SELECT TOP 10 * FROM EMPLOYEES
ORDER BY BIRTHDATE

-- 2) EMPLOYEES TABLOSUNDA EN GENÇ 10 KİŞİYİ GETİREN SQL SORGUSU 
SELECT TOP 10 * FROM EMPLOYEES
ORDER BY BIRTHDATE DESC

-- 3) EMPLOYEES TABLOSUNDA KADINLAR ARASINDAN EN GENÇ 10 KİŞİYİ GETİREN SQL SORGUSU 
SELECT TOP 10 * FROM EMPLOYEES
WHERE GENDER = 'FEMALE'
ORDER BY BIRTHDATE DESC

-- 4) EMPLOYEES TABLOSUNDA ERKEKLER ARASINDAN EN YAŞLI %10'LUK KISMI GETİREN SQL SORGUSU
SELECT TOP 10 PERCENT * FROM EMPLOYEES
WHERE GENDER = 'MALE'
ORDER BY BIRTHDATE

-- 5) EMPLOYEES TABLOSUNDA ERKEKLER ARASINDAN ADI 'A' İLE BAŞLAYAN İLK 5 KİŞİYİ GETİREN SQL SORGUSU 
SELECT TOP 5 * FROM EMPLOYEES
WHERE 
GENDER = 'MALE'
AND
FULLNAME LIKE 'A%'




